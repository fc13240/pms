package com.lotut.pms.domain;

public class SharePatentDoc {
	private long patentDoc;
	private int shareBy;
	private int shareTo;
	public long getPatentDoc() {
		return patentDoc;
	}
	public void setPatentDoc(long patentDoc) {
		this.patentDoc = patentDoc;
	}
	public int getShareBy() {
		return shareBy;
	}
	public void setShareBy(int shareBy) {
		this.shareBy = shareBy;
	}
	public int getShareTo() {
		return shareTo;
	}
	public void setShareTo(int shareTo) {
		this.shareTo = shareTo;
	}
	
	
}
